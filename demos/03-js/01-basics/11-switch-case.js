const city = 'New Delhi';

switch( city ) {
    case 'Mumbai':
        console.log( 'Gateway of India' );
    case 'New Delhi':
        console.log( 'India Gate' );
        break;
    case 'Agra':
        console.log( 'Taj Mahal' );
        break;
    case 'Hyderabad':
        console.log( 'Charminar' );
        break;
    default:
        console.log( 'I dont know' );
}